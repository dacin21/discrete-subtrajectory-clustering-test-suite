/**
 * The variables keep track of the current style that is being applied.
 * The constants keep track of the different options we have.
 */
let __mapsThemeStyleCode = [];
let __mapsFilterStyleCode = [];
let __mapsStyle = [];
const mapsThemesStrings = ['normal', 'silver', 'grey', 'white', 'ski', 'retro', 'dark', 'night', 'aubergine'];
const mapsOptionsStrings = ['normal', 'hideLandsmarks', 'hideAll'];


/**
 * This function changes the map type.
 *
 * Google Maps allows 4 map types, any of them can be selected and applied here.
 * @param mapType, represents which map type should be shown
 */
function changeMapType(mapType) {
    if (SOURCES.hasOwnProperty(mapType)) {
        tileLayer.setSource(SOURCES[mapType]);
    } else {
        console.warn('Error occured. Map type ' + mapType + ' does not exist.');
        alert('The selected map type does not exist.');
    }
    return;


    if (['roadmap', 'satellite', 'hybrid', 'terrain'].indexOf(mapType) >= 0) {
        map.setMapTypeId(mapType);
    } else {
        console.log('Error occured. Map type ' + mapType + ' does not exists.');
        alert('This map type does not exists.')
    }
}

/**
 * Sets the map theme to a theme present in mapsThemesStrings.
 * @param theme, the theme to be applied.
 */
function setMapTheme(theme) {
    return;

    if (mapsThemesStrings.indexOf(theme) >= 0) {
        __mapsThemeStyleCode = mapsThemesVariables[mapsThemesStrings.indexOf(theme)];
    } else {
        if (theme !== '') {
            console.log('theme: ', theme, ' not recognized');
        }
    }
    updateMap();
}

/**
 * Sets the number of map POI info that should be applied.
 * @param option, how much of the POI should be applied
 */
function setMapPoi(option) {
    return;

    __mapsFilterStyleCode = [];
    if (mapsOptionsStrings.indexOf(option) >= 0) {
        let index = mapsOptionsStrings.indexOf(option);
        for (let i = index; i > 0; i--) {
            __mapsFilterStyleCode = __mapsFilterStyleCode.concat(mapsOptionsVariables[i]);
        }
    } else {
        if (option !== '') {
            console.log('option: ', option, ' not recognized');
        }
    }
    updateMap();
}

/**
 * Here we apply the actual settings we choose.
 */
function updateMap() {
    map.render();
    return;
    __mapsStyle = [];
    __mapsStyle = __mapsStyle.concat(__mapsFilterStyleCode);
    __mapsStyle = __mapsStyle.concat(__mapsThemeStyleCode);
    if (typeof map !== 'undefined') {
        map.setOptions({styles: __mapsStyle});
    }
}

